-- 연습문제 38 (SALGRADE 테이블과 조인, 비동등조인)
SELECT E.EMPNO, E.ENAME, E.SAL, S.GRADE
FROM EMP E
JOIN salgrade S
ON E.SAL BETWEEN S.LOSAL AND S.HISAL;


