-- 연습문제 40
SELECT ENAME, DEPTNO,
    DECODE(DEPTNO, 10, '총무부',
                   20, '개발부',
                   30, '영업부') 부서명
FROM EMP;

SELECT ENAME, DEPTNO,
    CASE DEPTNO WHEN 10 THEN '총무부'
                WHEN 20 THEN '개발부'
                WHEN 30 THEN '영업부'
    END "부서명"
FROM EMP;